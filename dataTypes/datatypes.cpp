#include<iostream>
#include<string>
using namespace std;
int main(){
    int a = 5; //interger variable
    float b = 10.5; // float variable
    double c = 20.99; // double variable
    bool d = true; // boolean variable
    char f = 'A'; // character variable
    
    //to use string, we need to include the string header
    string e = "Hello"; // string variable
    



    cout << "Integer value: " << a << endl;
    cout << "Float value: " << b << endl;
    cout << "Double value: " << c << endl;
    cout << "Boolean value: " << d << endl;
    cout << "String value: " << e << endl;
    cout << "Character value: " << f << endl;
    return 0;
}